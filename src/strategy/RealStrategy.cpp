#include <strategy/RealStrategy.h>
#include "Exceptions.h"
#include <spdlog/spdlog.h>
#include <spdlog/fmt/ostr.h>

Move RealStrategy::callRaiseOrFold(unsigned int bb)
{
    Move m;

    if (m.first != Action::Call || m.first != Action::Raise || m.first != Action::Fold)
    {
        spdlog::error("Cannot use {} move generated by strategy, expected call, raise or fold!", m);
        throw WrongPlayerMoveError();
    }

    return m;
}

Move RealStrategy::checkOrBet()
{
    Move m;

    if (m.first != Action::Check || m.first != Action::Bet)
    {
        spdlog::error("Cannot use {} move generated by strategy, expected check or bet!", m);
        throw WrongPlayerMoveError();
    }

    return m;
}
